// MathExample.java

public class MathExample {
    public static void main(String[] args) {
        int numberSQRT = 16;
        int numberPOW = 2;
        int numberABS = -10;

        System.out.println("The sqrt of 16 is: " + Math.sqrt(numberSQRT));
        System.out.println("2 to the power of 4 is: " + Math.pow(numberPOW, 4));
        System.out.println("The absolute value of " + numberABS + " is: " + Math.abs(numberABS));

    }
}