// ReferenceTypeExample.java

public class ReferenceTypeExample {
    public static void main(String[] args) {
        String message = "Hello, Java!";
        System.out.println(message); // prints the message

        // string methods
        // https://www.geeksforgeeks.org/java/java-string-methods/
        System.out.println("Length: " + message.length());
        System.out.println("Uppercase: " + message.toUpperCase());
        System.out.println("Substring: " + message.substring(2, 6));
        // This method returns the index within the string of the
        // first occurrence of the specified string,
        // starting at the specified index
        System.out.println(str.indexOf("l", 4));

    }
}